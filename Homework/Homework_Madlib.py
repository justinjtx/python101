# Input function for noun, name, and a verb to fit in a story

def story(noun, name, verb):
    print(f"There was once a {noun} that suddenly came to life, called themselves {name}" )
    print(f"and starting {verb} around the town. {name} decided that {verb} was unaccepted" )
    print("and decided to move to the countryside and live a life of solitude") 






noun = input("noun:")
