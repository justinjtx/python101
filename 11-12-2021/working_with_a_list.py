numbers = []                    # start with an empty list then print
print(numbers)

numbers = [1, 2, 3, 4, 5]       # add five numbers to a new list, printing after each addition
print(numbers)

numbers.remove(1)               # remove one number from the beginning and print the numbers
print(numbers)

numbers[1] = 9                  # Update the second value to a new number and print the numbers
print(numbers)

numbers.remove(5)               # remove one number from the end and print the numbers
print(numbers)
